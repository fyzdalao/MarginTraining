class LDAMScheduler():
    def __init__(self, optimizer, lr):
        self.optimizer = optimizer
        self._step_count = 1
        self.lr = lr

    def step(self):
        self._step_count += 1
        if self._step_count <= 5:
            lr = self.lr * self._step_count / 5
        elif self._step_count > 180:
            lr = self.lr * 0.0001
        elif self._step_count > 160:
            lr = self.lr * 0.01
        else:
            lr = self.lr
        for param_group in self.optimizer.param_groups:
            param_group['lr'] = lr


def get_margin(loader, model, device='cuda'):
    model.eval()
    ss = np.array([])
    for x, y in loader:
        x, y = x.to(device), y.to(device)
        sims = model.get_margin(x)
        one_hot_label = F.one_hot(y, sims.size(1)).float()
        sim = torch.sum(sims * one_hot_label, dim=1).detach().cpu().numpy()
        ss = np.r_[ss, sim]
    return ss

class AverageMeter(object):

    def __init__(self, name, fmt=':f'):
        self.name = name
        self.fmt = fmt
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count

    def __str__(self):
        fmtstr = '{name} {val' + self.fmt + '} ({avg' + self.fmt + '})'
        return fmtstr.format(**self.__dict__)


def accuracy(output, target, topk=(1,)):
    with torch.no_grad():
        maxk = max(topk)
        batch_size = target.size(0)

        _, pred = output.topk(maxk, 1, True, True)
        pred = pred.t()
        correct = pred.eq(target.view(1, -1).expand_as(pred))

        res = []
        for k in topk:
            correct_k = correct[:k].contiguous().view(-1).float().sum(0, keepdim=True)
            res.append(correct_k.mul_(100.0 / batch_size))
        return res